// print odd numbers 1 to 15
for(let i=1; i<=15; i=i+2) {
   console.log(i);
}
// even numbers
for(let i=2; i<=10; i=i+2) {
   console.log(i);
}
// reverse even numbers
for(let i=10; i>=2; i=i-2) {
   console.log(i);
}
