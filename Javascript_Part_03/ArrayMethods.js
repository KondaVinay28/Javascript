// Push, unshift,pop, shift
let devices = ["Airpods", "Speakers", "Wired-Earphone" ];
devices.push("Android Phone"); //adds to the end
devices.pop("Android Phone"); //removes from the end
devices.unshift("Beats by Apple"); //adds to the start
devices.shift("Beats by Apple"); //removes from the start
console.log(devices);
console.log(devices.shift()); //this removes first item from an array and stores it as separate
console.log(devices);
